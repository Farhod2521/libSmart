class UzTranslit:
    _cyrillic_to_latin_map = {
        'А': 'A', 'а': 'a', 'Б': 'B', 'б': 'b',
        'В': 'V', 'в': 'v', 'Г': 'G', 'г': 'g',
        'Д': 'D', 'д': 'd', 'Е': 'E', 'е': 'e',
        'Ё': 'Yo', 'ё': 'yo', 'Ж': 'J', 'ж': 'j',
        'З': 'Z', 'з': 'z', 'И': 'I', 'и': 'i',
        'Й': 'Y', 'й': 'y', 'К': 'K', 'к': 'k',
        'Л': 'L', 'л': 'l', 'М': 'M', 'м': 'm',
        'Н': 'N', 'н': 'n', 'О': 'O', 'о': 'o',
        'П': 'P', 'п': 'p', 'Р': 'R', 'р': 'r',
        'С': 'S', 'с': 's', 'Т': 'T', 'т': 't',
        'У': 'U', 'у': 'u', 'Ф': 'F', 'ф': 'f',
        'Х': 'X', 'х': 'x', 'Ц': 'S', 'ц': 's',
        'Ч': 'Ch', 'ч': 'ch', 'Ш': 'Sh', 'ш': 'sh',
        'Щ': 'Sh', 'щ': 'sh', 'Ъ': '', 'ъ': '',
        'Ы': 'I', 'ы': 'i', 'Ь': '', 'ь': '',
        'Э': 'E', 'э': 'e', 'Ю': 'Yu', 'ю': 'yu',
        'Я': 'Ya', 'я': 'ya', 'Ў': "O'", 'ў': "o'",
        'Ғ': "G'", 'ғ': "g'", 'Қ': 'Q', 'қ': 'q',
        'Ҳ': 'H', 'ҳ': 'h'
    }

    @classmethod
    def to_latin(cls, text):
        for cyr, lat in cls._cyrillic_to_latin_map.items():
            text = text.replace(cyr, lat)
        return text

